export function normalizeMatch(m: MatchType): NormalizedMatch {
  const d = m.description ?? "";
  return { type: m.type, title: m.title, description: d,
    shortDescription: m.shortDescription ?? d.slice(0, 60).replace(/\n/g, "") + (d.length > 60 ? "…" : ""),
    traits: m.traits ?? m.type + " has a stable way of expressing and relating.",
    chemistry: m.chemistry ?? m.why ?? d, frictionPoint: m.frictionPoint ?? m.why ?? d,
    howToGetAlong: m.howToGetAlong ?? "保持清晰的表达，给彼此调整的空间。",
    possibleFriction: m.possibleFriction ?? "提前说明你的需求，效果远好于互相猜测。",
  };
}
